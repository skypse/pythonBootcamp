# Declarando variáveis
nome_pessoa = "Gabriel"
carro = "Fusca"
ano_carro = 2005

# Utilizando valores padrões
print(f"Oi eu sou o {nome_pessoa}, tenho um {carro} do ano {ano_carro}!")
# --> Oi eu sou o Gabriel, tenho um Fusca do ano 2005!

# Alterando valores
nome_pessoa = "Amaral"
carro = "BYD"
ano_carro = 2024

# Valores alterados
print(f"Oi eu sou o {nome_pessoa}, tenho um {carro} do ano {ano_carro}!")
# --> Oi eu sou o Amaral, tenho um BYD do ano 2024!