# Declarando uma por uma
idade = 19
nome = "Gabriel"
print(f"Meu nome é {nome} e eu tenho {idade} ano(s) de idade.")
# --> Meu nome é Gabriel e eu tenho 19 ano(s) de idade.

# Várias variáveis na mesma linha
idade, nome = (19, "Gabriel")
print(f"Meu nome é {nome} e eu tenho {idade} ano(s) de idade.")
# --> Meu nome é Gabriel e eu tenho 19 ano(s) de idade.