curso = "Curso de Python"
frutas = ["laranja", "uva", "limão"]
saques = [1500, 100]

# Tem "Python" em curso? SIM = true
print("Python" in curso) # true

# Tem "maça" em frutas? NÃO = TRUE
# Aqui retorna true, pois estamos utilizando o 'NOT IN'
print("maça" not in frutas)

# Tem 200 em saques? SIM = true
print(200 in saques) # True